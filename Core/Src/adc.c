/**
 * @brief  获取ADC通道的平均电压值
 * @param  hadc    : 指向ADC外设句柄的指针（包含ADC配置信息）
 * @param  channel : 目标ADC通道号（如ADC_CHANNEL_0）
 * @param  n       : 采样次数（需≥1）
 * @param  Vref    : ADC参考电压值（单位：伏特）
 * @return double  : 转换后的平均电压值（单位：伏特）
 */
double GetADC_Average(ADC_HandleTypeDef *hadc, uint32_t channel, uint8_t n, double Vref) 
{
    if (n == 0 || hadc == NULL) return 0.0f;
    
    ADC_ChannelConfTypeDef sConfig = {0};
    uint32_t sum = 0;
    const uint32_t timeout = 10; // 10ms��ʱ

    sConfig.Channel = channel;
    sConfig.Rank = 1;                     // ����ͨ�����е�һλ
    sConfig.SamplingTime = ADC_SAMPLETIME_480CYCLES; // Ĭ�����ٲ���
    if (HAL_ADC_ConfigChannel(hadc, &sConfig) != HAL_OK) {
        return 0.0f; 
    }

    // ����ADC���ֵ���Զ�����ֱ��ʣ�
    uint32_t max_value = (1 << (12 + 4*(hadc->Init.Resolution != ADC_RESOLUTION_12B))) - 1;
    /* 
        ADC_RESOLUTION_12B -> 4095 (1<<12-1)
        ADC_RESOLUTION_10B -> 1023 (1<<10-1)
        ADC_RESOLUTION_8B  -> 255  (1<<8-1)
    */

    for (int i = 0; i < n; i++) {
        HAL_ADC_Start(hadc);
        if (HAL_ADC_PollForConversion(hadc, timeout) == HAL_OK) {
            sum += HAL_ADC_GetValue(hadc); // 
        }
        HAL_ADC_Stop(hadc);
    }
    return (sum * Vref) / (n * max_value); // ͳһת����ѹ
}

double GetADC_Max(uint16_t *adc_array, uint16_t n, double Vref, uint32_t sampling_rate)
{
    if (n == 0 || adc_array == NULL) 
        return 0.0;
    
    // 1. 在数组中查找最大值
    uint16_t max_val = 0;
    for (int i = 0; i < n; i++) {
        if (adc_array[i] > max_val) {
            max_val = adc_array[i];
        }
    }
    
    // 2. 计算实际电压值
    // ADC最大理论值（全量程）
    // 注意：采样率会影响有效分辨率（ENOB），但最大值仍由硬件决定
    const uint32_t full_scale = (sampling_rate > 1000000) ? 65535 : 4095;
    
    // 3. 转换为电压值
    return (max_val * Vref) / full_scale;
}

double GetADC_Vpp(uint16_t *adc_array, uint16_t n, double Vref, uint32_t sampling_rate)
{
    if (n == 0 || adc_array == NULL) 
        return 0.0;
    
    // 1. 在数组中查找最大值
    uint16_t max_val = 0;
	uint16_t min_val = 4097;
    for (int i = 0; i < n; i++) {
        if (adc_array[i] > max_val) {
            max_val = adc_array[i];
        }
		if (adc_array[i] < min_val) {
			min_val = adc_array[i];
		}
    }
    
	const uint32_t full_scale = (sampling_rate > 1000001) ? 65535 : 4095;
    // 2. 转换为电压值
    return ((max_val-min_val) * Vref) / full_scale;
}

